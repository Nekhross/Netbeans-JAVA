    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        int fila = jtficha.getSelectedRow();

        String id_ficha = (String) jtficha.getValueAt(fila, 0);
        String destinatario = (String) jtficha.getValueAt(fila, 1);
        String areas = (String) jtficha.getValueAt(fila, 2);
        String areas2 = (String) jtficha.getValueAt(fila, 2);
        String observaciones = (String) jtficha.getValueAt(fila, 3);
        String resultados = (String) jtficha.getValueAt(fila, 4);
        interfaz_registrointervenciones registroficha = new interfaz_registrointervenciones();

        registroficha.cmbdestinatario.setSelectedItem(destinatario);
        registroficha.cmbdestinatario.setSelectedItem(areas);
        registroficha.txtotro.setText(areas2);
        registroficha.txtobservaciones.setText(observaciones);
        registroficha.txtresultados.setText(resultados);
       
        listarfichas();
        registroficha.btnactualizar.setEnabled(true);
        registroficha.btnregistrar.setEnabled(false);
        registroficha.btnregistrar.setText("NO DISPONIBLE");
        registroficha.lblid.setText(id_ficha);

        registroficha.show();
    }//GEN-LAST:event_btnactualizarActionPerformed

