    public void listarfichas() {

        try {
            Conexion conexion = new Conexion();
            Statement st = conexion.conexion().createStatement();

            ResultSet rs = st.executeQuery("select * from fichaintervencion");

            DefaultTableModel tablamodelo = (DefaultTableModel) jtficha.getModel();
            tablamodelo.setColumnCount(0);
            tablamodelo.addColumn("ID DE LA FICHA");
            tablamodelo.addColumn("DESTINATARIO");
            tablamodelo.addColumn("AREAS");
            tablamodelo.addColumn("OBSERVACIONES");
            tablamodelo.addColumn("RESULTADOS");
            tablamodelo.setRowCount(0);

            while (rs.next()) {

                String[] registros = new String[5];
                registros[0] = rs.getString("id_ficha");
                registros[1] = rs.getString("destinatario");
                registros[2] = rs.getString("areas");
                registros[3] = rs.getString("observaciones");
                registros[4] = rs.getString("resultados");
                tablamodelo.addRow(registros);  
            }
            jtficha.setModel(tablamodelo);

        } catch (SQLException ex) {
            Logger.getLogger(interfaz_fichaintervencion.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

